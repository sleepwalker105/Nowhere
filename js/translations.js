// Apply translations function
function applyTranslations(language) {
    const langData = translations[language];
    if (!langData) return;
    
    console.log('üåç Applying translations for:', language);
    
    // Update all elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (langData[key]) {
            element.innerHTML = langData[key];
        }
    });
    
    // Update placeholders
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        if (langData[key]) {
            element.placeholder = langData[key];
        }
    });
    
    // Update page title if exists
    const titleElement = document.querySelector('title[data-translate]');
    if (titleElement) {
        const key = titleElement.getAttribute('data-translate');
        if (langData[key]) {
            document.title = langData[key];
        }
    }
    
    // Update projects count if on projects page
    if (window.location.pathname.includes('projects.html')) {
        setTimeout(() => {
            const countElement = document.getElementById('projects-count');
            if (countElement && window.projects) {
                const total = window.projects.length;
                if (language === 'en') {
                    countElement.textContent = `${total} projects`;
                } else {
                    countElement.textContent = `${total} prosjekter`;
                }
            }
        }, 100);
    }
    
    console.log('‚úÖ Translations applied successfully');
}

// Make functions available globally
window.translations = translations;
window.applyTranslations = applyTranslations;

console.log('üåç Enhanced translations loaded!');
